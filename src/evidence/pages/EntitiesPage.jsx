function relationshipTargetChoices(entity, entities, searchedEntities) {
  const map = new Map();
  [...(searchedEntities || []), ...(entities || [])]
    .filter((item) => item?.person_id && item.person_id !== entity?.person_id)
    .forEach((item) => map.set(item.person_id, item));
  return [...map.values()].sort((a, b) => String(a.canonical_name || '').localeCompare(String(b.canonical_name || '')));
}

function relationshipTargetMatches(entity, entities, searchedEntities, query) {
  const search = normalizeIdentityText(query);
  if (!search) {
    return relationshipTargetChoices(entity, entities, searchedEntities).slice(0, 8);
  }
  const map = new Map();
  (searchedEntities || [])
    .filter((item) => item?.person_id && item.person_id !== entity?.person_id)
    .forEach((item) => map.set(item.person_id, item));
  (entities || [])
    .filter((item) => item?.person_id && item.person_id !== entity?.person_id && entityMatchesSearch(item, search))
    .forEach((item) => {
      if (!map.has(item.person_id)) {
        map.set(item.person_id, item);
      }
    });
  return [...map.values()].slice(0, 8);
}

function selectedRelationshipTarget(personId, entities, searchedEntities) {
  return [...(searchedEntities || []), ...(entities || [])].find((item) => item?.person_id === personId) || null;
}

function isVagueKinshipAlias(value) {
  const normalized = normalizeIdentityText(value);
  if (VAGUE_KINSHIP_ALIASES.has(normalized)) {
    return true;
  }
  const parts = normalized.split(' ');
  return parts.length === 2 && ['my', 'his', 'her', 'our', 'their'].includes(parts[0]) && VAGUE_KINSHIP_ALIASES.has(parts[1]);
}

function vagueKinshipAliases(values) {
  return (values || []).filter((value) => isVagueKinshipAlias(value));
}

function parseCommaList(value) {
  return String(value || '')
    .split(/[\n,]+/)
    .map((item) => item.trim())
    .filter(Boolean);
}

function roleResolutionNeedsReview(preview) {
  const items = preview?.items || [];
  return items.some((item) => item.decision !== 'auto_apply');
}

function rolesFromResolution(preview, fallbackRoles, mode = 'suggested') {
  const items = preview?.items || [];
  if (!items.length) {
    return fallbackRoles;
  }
  return items.map((item, index) => {
    if (mode === 'original') {
      return item.input_role || fallbackRoles[index] || '';
    }
    return item.normalized_role || item.input_role || fallbackRoles[index] || '';
  }).filter(Boolean);
}

